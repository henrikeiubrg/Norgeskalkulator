// Helper functions
function calculateHours(timeStr) {
    let hours = 0;
    const days = timeStr.match(/(\d+)\s*days?/i);
    const hrs = timeStr.match(/(\d+)\s*hours?/i);
    if (days) hours += parseInt(days[1], 10) * 24;
    if (hrs) hours += parseInt(hrs[1], 10);
    return hours;
}

function fetchSeaDistanceHTML(fromId, toId, speed) {
    return new Promise((resolve, reject) => {
        const url = `https://sea-distances.org/distances/ports/id_from/${fromId}/id_to/${toId}/speed/${speed}`;
        const req = https.get(
            url,
            {
                agent: HTTP_AGENT,
                headers: {
                    'User-Agent': 'Mozilla/5.0 (compatible; RouteCalc/1.0)',
                    Accept: 'text/html,application/xhtml+xml,application/xml;q=0.9,*/*;q=0.8',
                },
            },
            (res) => {
                const chunks = [];
                res.on('data', (c) => chunks.push(c));
                res.on('end', () => {
                    const html = Buffer.concat(chunks).toString();
                    if (res.statusCode && res.statusCode >= 200 && res.statusCode < 300) {
                        resolve(html);
                    } else {
                        reject(new Error(`Upstream ${res.statusCode}`));
                    }
                });
            }
        );
        req.on('error', (e) => reject(e));
        req.setTimeout(7000, () => req.destroy(new Error('Upstream timeout')));
    });
}

function parseRoutesWithRouting(fullHtml, maxRoutes = 3) {
    const results = [];

    const blocks = [];
    const reWay = /Way\s*#\s*\d+/gi;
    let m, indices = [];
    while ((m = reWay.exec(fullHtml)) !== null) indices.push(m.index);
    if (indices.length === 0) {
        blocks.push(fullHtml);
    } else {
        for (let i = 0; i < indices.length; i++) {
            const start = indices[i];
            const end = i + 1 < indices.length ? indices[i + 1] : fullHtml.length;
            blocks.push(fullHtml.slice(start, end));
        }
    }

    const coreRe = /Way #\s*(\d+)[\s\S]*?(\d{3,})\s*nautical miles[\s\S]*?(?:VIA\s*(?:<[^>]*>)*([^<\n]*?)(?:<[^>]*>)*)?[\s\S]*?(\d+)\s*knots[\s\S]*?(\d+\s*days?\s*\d*\s*hours?)/i;

    const cleanText = (s) => s.replace(/\s+/g, ' ').trim();

    const canonicalizeVia = (via) => {
        if (!via) return '';
        const v = via.toLowerCase();
        if (v.includes('panama')) return 'Panama Canal';
        if (v.includes('suez')) return 'Suez Canal';
        if (v.includes('good hope')) return 'Cape of Good Hope';
        if (v.includes('magellan')) return 'Strait of Magellan';
        if (v.includes('horn')) return 'Cape Horn';
        return cleanText(via);
    };

    function extractRouting(block) {
        const routingSection =
            /Routing[\s\S]{0,600}?<(?:ul|ol)[^>]*>([\s\S]*?)<\/(?:ul|ol)>/i.exec(block);
        if (routingSection) {
            const items = [...routingSection[1].matchAll(/<li[^>]*>([\s\S]*?)<\/li>/gi)]
                .map(m => cleanText(m[1].replace(/<[^>]+>/g, '')))
                .filter(Boolean);
            if (items.length >= 2) return items;
        }

        const tableSection =
            /Routing[\s\S]{0,800}?<table[^>]*>([\s\S]*?)<\/table>/i.exec(block);
        if (tableSection) {
            const cells = [...tableSection[1].matchAll(/<t[dh][^>]*>([\s\S]*?)<\/t[dh]>/gi)]
                .map(m => cleanText(m[1].replace(/<[^>]+>/g, '')))
                .filter(Boolean);
            const uniq = [...new Set(cells)];
            if (uniq.length >= 2) return uniq;
        }

        const anchors = [...block.matchAll(/<a[^>]*>([\s\S]*?)<\/a>/gi)]
            .map(m => cleanText(m[1]))
            .filter(Boolean);

        const locLike = anchors
            .filter(a => /^[A-Za-z .,'()-]{2,}$/.test(a))
            .filter(a => !/^(nautical miles|knots|hours?|days?)$/i.test(a));

        if (locLike.length >= 2) {
            const seen = new Set();
            const seq = [];
            for (const a of locLike) {
                const k = a.toLowerCase();
                if (!seen.has(k)) { seen.add(k); seq.push(a); }
            }
            if (seq.length >= 2) return seq;
        }

        const plain =
            /Routing[^<:]*[:\-]?\s*([\s\S]*?)(?:<\/?div|<\/?table|Way #|\d+\s*nautical miles|$)/i.exec(block);
        if (plain) {
            const raw = cleanText(plain[1].replace(/<[^>]+>/g, ' '));
            const parts = raw
                .split(/(?:->|,|\|| to | via )/i)
                .map(s => cleanText(s))
                .filter(s => s && s.length < 100);
            const uniq = [...new Set(parts)];
            if (uniq.length >= 2) return uniq;
        }

        return [];
    }

    for (const block of blocks) {
        const core = coreRe.exec(block);
        if (!core) continue;

        const distance = parseInt(core[2], 10);
        const via = canonicalizeVia(core[3] || '');
        const speed = parseInt(core[4], 10);
        const time = cleanText(core[5]);

        const routing = extractRouting(block);

        results.push({
            distance,
            via,
            speed,
            time,
            routing,
        });

        if (results.length >= maxRoutes) break;
    }

    if (results.length === 0 && /nautical miles/i.test(fullHtml)) {
        const distMatch = /(\d{3,})\s*nautical miles/i.exec(fullHtml);
        const timeMatch = /(\d+\s*days?\s*\d*\s*hours?)/i.exec(fullHtml);
        results.push({
            distance: distMatch ? parseInt(distMatch[1], 10) : 0,
            via: '',
            speed: 0,
            time: timeMatch ? cleanText(timeMatch[1]) : '',
            routing: [],
        });
    }

    return results;
}

// Netlify Function Handler
export async function handler(event, context) {
    const headers = {
        'Access-Control-Allow-Origin': '*',
        'Access-Control-Allow-Headers': 'Content-Type',
        'Access-Control-Allow-Methods': 'GET, POST, OPTIONS',
        'Content-Type': 'application/json',
    };

    if (event.httpMethod === 'OPTIONS') {
        return { statusCode: 200, headers, body: '' };
    }

    const path = event.path.replace('/.netlify/functions/server', '');

    try {
        // GET /api/ports
        if (path === '/api/ports' && event.httpMethod === 'GET') {
            return {
                statusCode: 200,
                headers,
                body: JSON.stringify({ 
                    success: true, 
                    count: Object.keys(portMapping).length, 
                    ports: portMapping 
                }),
            };
        }

        // GET /api/ports/search
        if (path.startsWith('/api/ports/search') && event.httpMethod === 'GET') {
            const q = (event.queryStringParameters?.q || '').toLowerCase().trim();
            if (!q) {
                return {
                    statusCode: 200,
                    headers,
                    body: JSON.stringify({ success: true, results: [] }),
                };
            }

            const results = [];
            for (const [id, name] of Object.entries(portMapping)) {
                if (name.toLowerCase().includes(q) || id.includes(q)) {
                    results.push({ id, name });
                }
            }
            
            results.sort((a, b) => {
                const as = a.name.toLowerCase().startsWith(q);
                const bs = b.name.toLowerCase().startsWith(q);
                if (as && !bs) return -1;
                if (!as && bs) return 1;
                return a.name.localeCompare(b.name);
            });

            return {
                statusCode: 200,
                headers,
                body: JSON.stringify({ success: true, results: results.slice(0, 30) }),
            };
        }

        // GET /api/route/:from/:to/:speed
        const routeMatch = path.match(/^\/api\/route\/([^\/]+)\/([^\/]+)\/([^\/]+)$/);
        if (routeMatch && event.httpMethod === 'GET') {
            const [, from, to, speedParam] = routeMatch;
            const spd = Number(speedParam) || DEFAULT_SPEED;

            console.log(`Fetching route: ${from} -> ${to} at ${spd} knots`);
            const html = await fetchSeaDistanceHTML(from, to, spd);
            const parsed = parseRoutesWithRouting(html, 3);
            
            const data = parsed.map(r => ({
                Time: r.time,
                'Time in Hours': calculateHours(r.time),
                Distance: r.distance,
                'Vessel Speed': r.speed,
                Via: r.via || 'Direct',
                Routing: r.routing,
                id_from: Number(from),
                id_to: Number(to),
                port_from: portMapping[from],
                port_to: portMapping[to]
            }));

            if (data.length === 0) {
                return {
                    statusCode: 502,
                    headers,
                    body: JSON.stringify({ 
                        success: false, 
                        error: 'No route data parsed from upstream HTML' 
                    }),
                };
            }

            return {
                statusCode: 200,
                headers,
                body: JSON.stringify({
                    success: true,
                    data,
                    ports: { 
                        from: portMapping[from], 
                        to: portMapping[to] 
                    }
                }),
            };
        }

        // GET /api/health
        if (path === '/api/health' && event.httpMethod === 'GET') {
            return {
                statusCode: 200,
                headers,
                body: JSON.stringify({ 
                    success: true, 
                    message: 'Server is running', 
                    portCount: Object.keys(portMapping).length 
                }),
            };
        }

        return {
            statusCode: 404,
            headers,
            body: JSON.stringify({ success: false, error: 'Not found' }),
        };

    } catch (error) {
        console.error('Function error:', error);
        return {
            statusCode: 500,
            headers,
            body: JSON.stringify({ 
                success: false, 
                error: 'Internal server error',
                details: error.message 
            }),
        };
    }
}